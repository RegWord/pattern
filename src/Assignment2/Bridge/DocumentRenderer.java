package Assignment2.Bridge;

// Это абстрактный класс, который связывает документ с рендеринг-движком.
// Типа как посредник: документ — это текст, а движок — это то, что его красиво рисует.
public abstract class DocumentRenderer {
    // Это рендеринг-движок, который мы будем использовать для отображения документа.
    // protected значит, что этот движок доступен для дочерних классов, но не для всех подряд.
    protected RenderEngine engine;

    // Конструктор, который принимает рендеринг-движок.
    // Когда мы создаем новый рендерер, мы говорим ему, каким движком он будет пользоваться.
    public DocumentRenderer(RenderEngine engine) {
        this.engine = engine;
    }

    // Абстрактный метод рендеринга (отображения) содержимого.
    // Классы, которые будут наследовать этот, должны сами решать, как они будут рендерить контент.
    public abstract void render(String content);

    // Задание: реализовать рендеринг документа с использованием переданного движка.
    // То есть, тут будет метод, который рендерит документ, используя конкретный движок.
    public abstract void renderDocument(String content);
    // Здесь потом напишем код, который скажет движку: "Отрисуй мне документ!"
}
